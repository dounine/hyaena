package com.dounine.hyaena.search;

public class Profile {

    private String version;
    private String createTime;
    private String updateTime;
    private String name;
    private String jdk;
    private String databaseKeys;
    private String serviceKeys;
    private String controllerKeys;
    private String viewKeys;
    private String struct;

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getJdk() {
        return jdk;
    }

    public void setJdk(String jdk) {
        this.jdk = jdk;
    }

    public String getDatabaseKeys() {
        return databaseKeys;
    }

    public void setDatabaseKeys(String databaseKeys) {
        this.databaseKeys = databaseKeys;
    }

    public String getServiceKeys() {
        return serviceKeys;
    }

    public void setServiceKeys(String serviceKeys) {
        this.serviceKeys = serviceKeys;
    }

    public String getControllerKeys() {
        return controllerKeys;
    }

    public void setControllerKeys(String controllerKeys) {
        this.controllerKeys = controllerKeys;
    }

    public String getViewKeys() {
        return viewKeys;
    }

    public void setViewKeys(String viewKeys) {
        this.viewKeys = viewKeys;
    }

    public String getStruct() {
        return struct;
    }

    public void setStruct(String struct) {
        this.struct = struct;
    }
}
